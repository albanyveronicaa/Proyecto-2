    private void verificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verificarActionPerformed
        try {
            if (this.jTextArea3.getText().length() > 0) {
                this.jTextArea3.setText("");
            }
            String titulo = this.jList2.getSelectedValue();
            String tituloSegmento = this.jList1.getSelectedValue();
            String texto = this.manejoDocumentos.getRabinKarp().buscar(titulo).texto;
            String segmento = this.manejoDocumentos.getRabinKarp().buscar(tituloSegmento).texto;
            if (texto != null && segmento != null) {
                if (texto.length() < segmento.length()) {
                    JOptionPane.showMessageDialog(rootPane, "El documento "
                            + "seleccionado es de menor longitud. "
                            + "No se puede verificar el texto y "
                            + "buscar si existe plagio");
                } else {
                    AlgoritmoRabinKarp a = new AlgoritmoRabinKarp();
                    String resultado = a.algoritmoRabinKarp(texto, segmento);
                    this.jTextArea3.setText(resultado);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Debe seleccionar el documento "
                    + "y el texto que desea verificar");
        }
    }//GEN-LAST:event_verificarActionPerformed


