    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        String buscador = this.jTextField2.getText();
        if (buscador.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Ingrese la "
                    + "palabra que desea buscar: ");
        } else {
            NodoHash resultado = this.tabla.buscarPalabra(buscador);
            if (resultado == null) {
                JOptionPane.showMessageDialog(rootPane, "No se encontró "
                        + "la palabra ingresada.");
            } else {
                if (resultado.getContarPalabras() == 1) {
                    JOptionPane.showMessageDialog(rootPane, "Se encontró '"
                            + resultado.getPalabra() + "' 1 vez.");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Se encontró '"
                            + resultado.getPalabra() + "' "
                            + resultado.getContarPalabras() + " veces.");
                }
            }
        }

    }

