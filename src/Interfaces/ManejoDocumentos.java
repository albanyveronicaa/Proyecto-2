    private void cargarArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cargarArchivoActionPerformed

        JOptionPane.showMessageDialog(null, "Por favor escoja el archivo que desea cargar al sistema.", "Cargar archivo", JOptionPane.INFORMATION_MESSAGE);

        JFileChooser fc = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.TXT", "txt");
        fc.setFileFilter(filter);
        int select = fc.showOpenDialog(this);
        if (select == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();
            this.jTextField1.setText(file.getAbsolutePath());
        }
    }//GEN-LAST:event_cargarArchivoActionPerformed

    private void elegirDocumentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_elegirDocumentoActionPerformed
        String pathToFile = this.jTextField1.getText();
        if (pathToFile.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Debe llenar el recuadro "
                    + "con la ruta o buscar un archivo");
        } else {
            LeerDocumento f = new LeerDocumento();
            try {
                Tabla tl = f.openFile(pathToFile, this.tabla, rabinkarp, top5);
                this.setTabla(tl.getTable());
                this.setTop5(tl.getTop5());
                this.setRabinKarp(tl.getKarpList());
                this.jTextArea1.setText(this.tabla.mostrar());
                this.jTextArea2.setText(this.top5.mostrar());
                this.jTextField1.setText("");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "El archivo cargado no "
                        + "pudo ser procesado por la plataforma. Por favor "
                        + "intente con otro archivo");
            }
        }
    }//GEN-LAST:event_elegirDocumentoActionPerformed

